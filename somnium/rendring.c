/*
   Linearly interpolate the position where an isosurface cuts
   an edge between two vertices, each with their own scalar value
*/

//static void interpolation(point3f *result, float isovalue, point3f *a, point3f *b, float va, float vb)
static void interpolation(point3f *point, vector3f *normal, float isovalue,
						  mc_grid_cell *cell, int a, int b)
{
	float t;

	if (ABS(isovalue - cell->value[a]) < EPSILON || 
		ABS(cell->value[a] - cell->value[b]) < EPSILON)
	{
		//result->x = a->x;
		//result->y = a->y;
		//result->z = a->z;
		*point = cell->point[a];
		*normal = cell->normal[a];
		v_normalize_c(normal);

		return;
	}
	if (ABS(isovalue - cell->value[b]) < EPSILON)
	{
		//result->x = b->x;
		//result->y = b->y;
		//result->z = b->z;
		*point = cell->point[b];
		*normal = cell->normal[b];
		v_normalize_c(normal);

		return;
	}
	t = (isovalue - cell->value[a]) / (cell->value[b] - cell->value[a]);
	point->x = cell->point[a].x + t * (cell->point[b].x - cell->point[a].x);
	point->y = cell->point[a].y + t * (cell->point[b].y - cell->point[a].y);
	point->z = cell->point[a].z + t * (cell->point[b].z - cell->point[a].z);

	normal->x = cell->normal[a].x + t * (cell->normal[b].x - cell->normal[a].x);
	normal->y = cell->normal[a].y + t * (cell->normal[b].y - cell->normal[a].y);
	normal->z = cell->normal[a].z + t * (cell->normal[b].z - cell->normal[a].z);
	v_normalize_c(normal);
}

/*
   Given a grid cell and an isolevel, calculate the triangular
   facets required to represent the isosurface through the cell.
   Return the number of triangular facets, the array "triangles"
   will be loaded up with the vertices at most 5 triangular facets.
	0 will be returned if the grid cell is either totally above
   of totally below the isolevel.
*/
//static int polygonise(triangle *triangles, mc_grid_cell *cell, float isovalue)
static int polygonise(mc_grid_cell *cell, float isovalue)
{
   int i, ntriang;
   int cubeindex;
   point3f vertlist[12];
   vector3f normlist[12]; 

   cubeindex = 0;
   if (cell->value[0] < isovalue) cubeindex |= 1;
   if (cell->value[1] < isovalue) cubeindex |= 2;
   if (cell->value[2] < isovalue) cubeindex |= 4;
   if (cell->value[3] < isovalue) cubeindex |= 8;
   if (cell->value[4] < isovalue) cubeindex |= 16;
   if (cell->value[5] < isovalue) cubeindex |= 32;
   if (cell->value[6] < isovalue) cubeindex |= 64;
   if (cell->value[7] < isovalue) cubeindex |= 128;

   /* Cube is entirely in/out of the surface */
   if (edgeTable[cubeindex] == 0)
      return(0);

   /* Find the vertices where the surface intersects the cube */
	if (edgeTable[cubeindex] & 1)
		//interpolation(&vertlist[0], isovalue, &cell->point[0], &cell->point[1], cell->value[0], cell->value[1]);
		interpolation(&vertlist[0], &normlist[0], isovalue, cell, 0, 1);
	if (edgeTable[cubeindex] & 2)
		//interpolation(&vertlist[1], isovalue, &cell->point[1], &cell->point[2], cell->value[1], cell->value[2]);
		interpolation(&vertlist[1], &normlist[1], isovalue, cell, 1, 2);
	if (edgeTable[cubeindex] & 4)
		//interpolation(&vertlist[2], isovalue, &cell->point[2], &cell->point[3], cell->value[2], cell->value[3]);
		interpolation(&vertlist[2], &normlist[2], isovalue, cell, 2, 3);
	if (edgeTable[cubeindex] & 8)
		//interpolation(&vertlist[3], isovalue, &cell->point[3], &cell->point[0], cell->value[3], cell->value[0]);
		interpolation(&vertlist[3], &normlist[3], isovalue, cell, 3, 0);
	if (edgeTable[cubeindex] & 16)
		//interpolation(&vertlist[4], isovalue, &cell->point[4], &cell->point[5], cell->value[4], cell->value[5]);
		interpolation(&vertlist[4], &normlist[4], isovalue, cell, 4, 5);
	if (edgeTable[cubeindex] & 32)
		//interpolation(&vertlist[5], isovalue, &cell->point[5], &cell->point[6], cell->value[5], cell->value[6]);
		interpolation(&vertlist[5], &normlist[5], isovalue, cell, 5, 6);
	if (edgeTable[cubeindex] & 64)
		//interpolation(&vertlist[6], isovalue, &cell->point[6], &cell->point[7], cell->value[6], cell->value[7]);
		interpolation(&vertlist[6], &normlist[6], isovalue, cell, 6, 7);
	if (edgeTable[cubeindex] & 128)
		//interpolation(&vertlist[7], isovalue, &cell->point[7], &cell->point[4], cell->value[7] ,cell->value[4]);
		interpolation(&vertlist[7], &normlist[7], isovalue, cell, 7, 4);
	if (edgeTable[cubeindex] & 256)
		//interpolation(&vertlist[8], isovalue, &cell->point[0], &cell->point[4], cell->value[0], cell->value[4]);
		interpolation(&vertlist[8], &normlist[8], isovalue, cell, 0, 4);
	if (edgeTable[cubeindex] & 512)
		//interpolation(&vertlist[9], isovalue, &cell->point[1], &cell->point[5], cell->value[1], cell->value[5]);
		interpolation(&vertlist[9], &normlist[9], isovalue, cell, 1, 5);
	if (edgeTable[cubeindex] & 1024)
		//interpolation(&vertlist[10], isovalue, &cell->point[2], &cell->point[6], cell->value[2], cell->value[6]);
		interpolation(&vertlist[10], &normlist[10], isovalue, cell, 2, 6);
	if (edgeTable[cubeindex] & 2048)
		//interpolation(&vertlist[11], isovalue, &cell->point[3], &cell->point[7], cell->value[3], cell->value[7]);
		interpolation(&vertlist[11], &normlist[11], isovalue, cell, 3, 7);

   /* Create the triangle */
   ntriang = 0;
   glBegin(GL_TRIANGLES);
   for (i = 0; triTable[cubeindex][i]!= -1; i += 3)
   {
		/*triangles[ntriang].point[0] = vertlist[triTable[cubeindex][i  ]];
		triangles[ntriang].point[1] = vertlist[triTable[cubeindex][i+1]];
		triangles[ntriang].point[2] = vertlist[triTable[cubeindex][i+2]];*/
		glNormal3fv(normlist[triTable[cubeindex][i]].xyz);
		glVertex3fv(vertlist[triTable[cubeindex][i]].xyz);
		glNormal3fv(normlist[triTable[cubeindex][i + 1]].xyz);
		glVertex3fv(vertlist[triTable[cubeindex][i + 1]].xyz);
		glNormal3fv(normlist[triTable[cubeindex][i + 2]].xyz);
		glVertex3fv(vertlist[triTable[cubeindex][i + 2]].xyz);
		ntriang++;
   }
   glEnd();

   return ntriang;
}

